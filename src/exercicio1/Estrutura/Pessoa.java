package src.exercicio1.estrutura;

public abstract class Pessoa implements Universidade {
    private String nome;
    private String cpf;

    public Pessoa() {
        this.nome = nome;
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
}
